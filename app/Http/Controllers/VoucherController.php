     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $voucher = new Voucher();
        $voucher->nama = $request->nama;
        $voucher->kodeVoucher = $request->nama;
        $voucher->nominal = $request->nominal;
        $voucher->maxPotongan = $request->potongan;
        $voucher->tanggalAktif = $request->aktif;
        $voucher->tanggalBerakhir = $request->berakhir;
        $voucher->kuota = $request->kuota;
        $voucher->save();

    }

